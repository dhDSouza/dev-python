# Pedindo para o usuário digitar uma palavra e guardando esse valor na variável 'palavra'.

palavra = input("Digite uma palavra: ")

# Mostrando a quantidade de letras na palavra digitada com a função len().
# Depois, mostrando a palavra toda em letras maiúsculas com a função upper().

print(f"A palavra digitada possui {len(palavra)} letras.")
print(f"A palavra digitada em letras maiúsculas é: {palavra.upper()}.")
